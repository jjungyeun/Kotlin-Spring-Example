	ID
);

ALTER TABLE ORDERS2 ADD CONSTRAINT FK_DELIVERY2_TO_ORDERS2_1 FOREIGN KEY (
	DELIVERY_ID
)
REFERENCES DELIVERY2 (
	ID
);

ALTER TABLE ORDER_ITEM2 ADD CONSTRAINT FK_ORDERS2_TO_ORDER_ITEM2_1 FOREIGN KEY (
	ORDER_ID
)
REFERENCES ORDERS2 (
	ID
);

ALTER TABLE ORDER_ITEM2 ADD CONSTRAINT FK_ITEM2_TO_ORDER_ITEM2_1 FOREIGN KEY (
	ITEM_ID
)
