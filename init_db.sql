	ID
);

ALTER TABLE DELIVERY2 ADD CONSTRAINT PK_DELIVERY2 PRIMARY KEY (
	ID
);

ALTER TABLE ORDER_ITEM2 ADD CONSTRAINT PK_ORDER_ITEM2 PRIMARY KEY (
	ID
);

ALTER TABLE ITEM2 ADD CONSTRAINT PK_ITEM2 PRIMARY KEY (
	ID
);

